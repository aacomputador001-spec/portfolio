document.addEventListener('DOMContentLoaded', () => {
    const langToggleBtn = document.getElementById('lang-toggle');
    let currentLang = 'pt'; // default

    langToggleBtn.addEventListener('click', () => {
        currentLang = currentLang === 'pt' ? 'en' : 'pt';

        // Update button text
        langToggleBtn.textContent = currentLang === 'pt' ? 'EN' : 'PT';

        const elements = document.querySelectorAll('[data-i18n], [data-i18n-aria]');

        // Add fading out class
        elements.forEach(el => {
            el.classList.add('i18n-fading');
        });

        // Wait for fade out transition (CSS is 0.25s)
        setTimeout(() => {
            elements.forEach(el => {
                const key = el.getAttribute('data-i18n');
                const ariaKey = el.getAttribute('data-i18n-aria');

                if (key && translations[currentLang][key]) {
                    // We use innerHTML to support the <i> tags in the hero title and footer span
                    el.innerHTML = translations[currentLang][key];
                }

                if (ariaKey && translations[currentLang][ariaKey]) {
                    el.setAttribute('aria-label', translations[currentLang][ariaKey]);
                }
            });

            // Re-apply current year in case footer was translated
            const yearSpan = document.getElementById('current-year');
            if (yearSpan) {
                yearSpan.textContent = new Date().getFullYear();
            }

            // Remove fading out class to trigger fade in
            elements.forEach(el => {
                el.classList.remove('i18n-fading');
            });

            // Re-initialize interactive title since its DOM was replaced
            if (window.initInteractiveTitle) {
                window.initInteractiveTitle();
            }

        }, 250);
    });
});
